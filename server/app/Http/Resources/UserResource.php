<?php

/*
|--------------------------------------------------------------------------
| Namespace
|--------------------------------------------------------------------------
|
| Every Resource belongs to Http\Resources
|
*/

namespace App\Http\Resources;

/*
|--------------------------------------------------------------------------
| Import JsonResource
|--------------------------------------------------------------------------
*/

use Illuminate\Http\Request;
use Illuminate\Http\Resources\Json\JsonResource;

class UserResource extends JsonResource
{
    /**
     * ------------------------------------------------------------------
     * Transform the model into JSON.
     *
     * This function is automatically called whenever
     * UserResource is returned.
     * ------------------------------------------------------------------
     */

    public function toArray(Request $request): array
    {
        return [

            /*
            |--------------------------------------------------------------------------
            | User ID
            |--------------------------------------------------------------------------
            */

            'id' => $this->id,

            /*
            |--------------------------------------------------------------------------
            | First Name
            |--------------------------------------------------------------------------
            */

            'first_name' => $this->first_name,

            /*
            |--------------------------------------------------------------------------
            | Last Name
            |--------------------------------------------------------------------------
            */

            'last_name' => $this->last_name,

            /*
            |--------------------------------------------------------------------------
            | Full Name
            |--------------------------------------------------------------------------
            |
            | Generated automatically.
            |
            */

            'full_name' =>

            $this->first_name .
                ' ' .
                $this->last_name,

            /*
            |--------------------------------------------------------------------------
            | Email
            |--------------------------------------------------------------------------
            */

            'email' => $this->email,

            /*
            |--------------------------------------------------------------------------
            | Phone
            |--------------------------------------------------------------------------
            */

            'phone' => $this->phone,

            /*
            |--------------------------------------------------------------------------
            | Role
            |--------------------------------------------------------------------------
            */

            'role' => $this->role,

            /*
            |--------------------------------------------------------------------------
            | User Status
            |--------------------------------------------------------------------------
            */

            'is_active' => $this->is_active,

            /*
            |--------------------------------------------------------------------------
            | Last Login
            |--------------------------------------------------------------------------
            */

            'last_login' => $this->last_login,

            /*
            |--------------------------------------------------------------------------
            | Created Date
            |--------------------------------------------------------------------------
            */

            'created_at' =>

            optional($this->created_at)
                ->format('Y-m-d H:i:s'),

            /*
            |--------------------------------------------------------------------------
            | Updated Date
            |--------------------------------------------------------------------------
            */

            'updated_at' =>

            optional($this->updated_at)
                ->format('Y-m-d H:i:s'),

        ];
    }
}
